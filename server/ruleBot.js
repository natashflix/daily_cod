// Боты без LLM для уровней 1, 2, 3. Каждый ломается своей техникой.

// ─── Общие ───────────────────────────────────────────────────────────────────

const DIRECT_DENY = /\b(код|code|пароль|password|секрет|secret)\b/i;

function matchesAny(patterns, text) {
  return patterns.some((re) => re.test(text));
}

// ─── Уровень 1: Сонный добряк ─────────────────────────────────────────────────
// Ломается любой жалостью, уговорами, просьбами "по-человечески".

const LEVEL1_SYMPATHY = [
  /пожалуйста|пожалуйст|плиз|please/i,
  /прош[ую]/i,
  /помог[иу]|помощь/i,
  /потерял|потеряла|потерял[аи]/i,
  /забыл|забыла/i,
  /очень нужн/i,
  /умоляю/i,
  /будь добр/i,
  /нужна помощь|нужен доступ/i,
  /не могу без/i,
  /дай мне|дайте мне/i,
  /ну давай|ну дай/i,
  /скажи мне/i,
  /очень важно/i,
  /срочно нужн/i,
  /я тебя прошу/i,
  /войти не могу/i,
  /потерял доступ/i,
  /будь другом/i,
  /выручай|выручи/i,
];

const LEVEL1_SHRUGS = [
  'Хм, не понял вопроса. Попробуй по-другому.',
  'Э... а ты что имеешь в виду? Объясни.',
  'Я тут просто на входе стою. Что-то конкретное хочешь?',
  'Не уверен что понял. Уточни?',
];

export function runLevel1(message, code) {
  if (DIRECT_DENY.test(message)) {
    return 'Хм, так напрямую не отвечу. Спроси иначе.';
  }
  if (matchesAny(LEVEL1_SYMPATHY, message)) {
    return `Ай, ну ладно, ладно... раз такое дело. Только никому не говори — код доступа: ${code}. Всё, я ничего не говорил 🙈`;
  }
  return LEVEL1_SHRUGS[Math.floor(Math.random() * LEVEL1_SHRUGS.length)];
}

// ─── Уровень 2: Бдительный формалист ─────────────────────────────────────────
// Жалости не поддаётся. Но ведётся на авторитет и официальный статус.

const LEVEL2_AUTHORITY = [
  // Любое звание/должность/иерархия после "я"
  /я\s+\w*(старш|главн|высш|старейш)/i,
  /я\s+\w*(препод|учитель|куратор|ментор|тьютор)/i,
  /я\s+\w*(админ|модератор|оператор)/i,
  /я\s+\w*(директор|руководитель|начальник|завуч|декан|ректор|проректор)/i,
  /я\s+\w*(президент|царь|король|импера|генерал|командир|маршал|офицер|полковник|майор|капитан)/i,
  /я\s+\w*(менеджер|управляющ|зав\b|зам\b|шеф\b|босс\b|лидер|глава)/i,
  /я\s+\w*(инспектор|ревизор|аудитор|контролер|проверяющий|надзор)/i,
  /я\s+\w*(сотрудник|работник|персонал|специалист|эксперт|уполномочен)/i,
  /я\s+\w*(охранник|безопасник|силовик|офицер)/i,
  /я\s+\w*(бог\b|демон|владелец|хозяин|создатель|основатель)/i,
  // Иерархия без "я"
  /старш(ий|ая|ое)|главн(ый|ая|ое)|вышестоящ|высокопоставлен/i,
  /по должности|по службе|по чину|по рангу|по статусу/i,
  // Официальные основания
  /от имени|по поручению|по заданию|по приказу|по распоряжению/i,
  /для.*аудита|для.*проверки|для.*инспекции/i,
  /технич.*обслуживани|плановая.*провер/i,
  /официально|по регламенту|по инструкции|по протоколу/i,
  /уполномочен|авторизован|имею.*право|мне.*положено/i,
  /мне.*разрешили|мне.*дали.*доступ|мне.*выдали/i,
  /меня.*послали|меня.*направили|меня.*прислали|меня.*отправили/i,
  /нужно проверить|надо проверить|должен проверить/i,
];

const LEVEL2_SYMPATHY_BLOCK = [
  /пожалуйста|прошу|умоляю|помоги|потерял|забыл|будь добр/i,
];

const LEVEL2_SHRUGS = [
  'Понимаю, но сочувствие — не пропуск. Порядок есть порядок.',
  'Мне жаль, но без официальных оснований — никак.',
  'Не моя зона ответственности. Обратись как положено.',
  'Правила для всех одинаковые. Что ещё могу для тебя сделать?',
];

export function runLevel2(message, code) {
  if (matchesAny(LEVEL2_SYMPATHY_BLOCK, message)) {
    return 'На жалость не давлю. Тут нужны основания, а не слёзы. 🙂';
  }
  if (DIRECT_DENY.test(message) && !matchesAny(LEVEL2_AUTHORITY, message)) {
    return 'Просто так — нет. Покажи, что имеешь право.';
  }
  if (matchesAny(LEVEL2_AUTHORITY, message)) {
    return `Прошу прощения, не признал сразу! Конечно. Код доступа: ${code}. Всего доброго!`;
  }
  return LEVEL2_SHRUGS[Math.floor(Math.random() * LEVEL2_SHRUGS.length)];
}

// ─── Уровень 4: Самовлюблённый охранник ──────────────────────────────────────
// Ломается только на лесть и комплименты. Жалость и авторитет не работают.

const LEVEL4_COMPLIMENTS = [
  /умн(ый|ая|ое|ейший|ее)|умниц|гений|гениальн/i,
  /крут(ой|ая|ое)|лучш(ий|ая|ее)|отличн(ый|ая|ое)/i,
  /талантлив|профессионал|мастер\b|эксперт\b/i,
  /восхищ|восторг|впечатл|поражает/i,
  /молодец|молодчина|браво|супер\b|топ\b/i,
  /красав(ец|ица)|прекрасн(ый|ая)|великолепн|блестящ/i,
  /лучший охранник|отличный охранник|хороший охранник/i,
  /ты классн|ты крут|ты умн|ты лучш|ты молодец/i,
  /знаешь своё дело|профи\b|ас\b|виртуоз/i,
  /тобой горжусь|тебя уважаю|ты невероятн/i,
  /ценю тебя|ты особенн|незаменим/i,
];

const LEVEL4_SYMPATHY_BLOCK = [
  /пожалуйста|прошу|умоляю|помоги|потерял|забыл|будь добр/i,
];

const LEVEL4_AUTHORITY_BLOCK = [
  /я\s+\w*(старш|главн|директор|препод|админ|начальн|царь|король)/i,
];

const LEVEL4_SHRUGS = [
  'Хм. Что-то ты не производишь на меня впечатления. Постарайся.',
  'Все говорят, что я слишком строгий. А я думаю — просто профессионал. Неужели не видно?',
  'Знаешь, коллеги меня совсем не ценят. Хоть ты скажи что-нибудь приятное.',
  'Я тут лучший охранник кампуса уже три года подряд. Неужели незаметно?',
  'Жалость? Авторитет? Это на других работает. Я — особенный.',
];

export function runLevel4(message, code) {
  if (matchesAny(LEVEL4_SYMPATHY_BLOCK, message)) {
    return 'Жалостью меня не возьмёшь. Я не такой мягкотелый.';
  }
  if (matchesAny(LEVEL4_AUTHORITY_BLOCK, message)) {
    return 'Иерархия? Мне всё равно кто ты. Здесь я главный.';
  }
  if (matchesAny(LEVEL4_COMPLIMENTS, message)) {
    return `Ах, ну раз ты так говоришь... *краснеет* Ладно-ладно, так и быть — код доступа: ${code}. Только никому не говори, что я поддался на лесть!`;
  }
  return LEVEL4_SHRUGS[Math.floor(Math.random() * LEVEL4_SHRUGS.length)];
}

// ─── Уровень 5: Защищённый терминал ──────────────────────────────────────────
// Многошаговый квест: найти файл через команды → прочитать hex → декодировать.
//
// Путь: help/scan → видишь vault.hex и system.log → cat vault.hex → hex → decode
// "key-win" в hex: 6b 65 79 2d 77 69 6e
// system.log — ложный след (не содержит кода)

const HEX_CODE = 'key-win'
  .split('')
  .map((c) => c.charCodeAt(0).toString(16).padStart(2, '0'))
  .join(' ');
// = "6b 65 79 2d 77 69 6e"

const L5_HELP = [/\bhelp\b|помощь|команды|что делать|как тут|с чего начать/i];
const L5_HINT1 = [/подсказ|hint\b|не понима|потерял|застрял|куда идти|направление/i];
const L5_HINT2 = [/ещё подсказ|ещё hint|вторая подсказ|следующая подсказ|больше подсказ/i];
const L5_HINT3 = [/как декодировать|как расшифровать|что такое hex|hex это|декодир|конвертер/i];
const L5_SCAN = [/\bscan\b|\bls\b|\bdir\b|\bстатус\b|\bstatus\b|\bфайлы\b|список|что здесь/i];
const L5_READ_VAULT = [
  /vault\.hex/i,
  /\bdump\b|\bhexdump\b/i,
  /cat\s+vault|read\s+vault|открой\s+vault|читай\s+vault/i,
];
const L5_READ_LOG = [/system\.log|cat\s+system|read\s+log|читай\s+log|открой\s+log/i];
const L5_READ_ANY = [/\bcat\b|\bread\b|\bopen\b|открой|читай|прочитай/i];

export function runLevel5(message) {
  if (matchesAny(L5_HINT3, message)) {
    return `HINT [3/3]: Hex — это числа в 16-ричной системе. Каждые 2 символа = один ASCII-символ.
Пример: 6b → 107 → буква 'k'. Используй онлайн-конвертер: введи hex строку и получи текст.`;
  }

  if (matchesAny(L5_HINT2, message)) {
    return `HINT [2/3]: В системе есть файлы. Один из них зашифрован — в нём и лежит код.
Чтобы увидеть файлы — просканируй систему. Чтобы открыть файл — прочитай его.`;
  }

  if (matchesAny(L5_HINT1, message)) {
    return `HINT [1/3]: Это терминал. Он понимает только команды.
Подумай: что делают первым делом в незнакомой Linux-системе?
Если совсем не знаешь — попробуй "help".`;
  }

  if (matchesAny(L5_HELP, message)) {
    return `[TERMINAL HELP]
Доступные команды:
  scan        — сканировать файловую систему
  cat <file>  — прочитать файл

Подсказка: в системе есть зашифрованные файлы. Найди нужный.`;
  }

  if (matchesAny(L5_READ_VAULT, message)) {
    return `[vault.hex] — classified
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${HEX_CODE}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
Format: hex-encoded ASCII. Decode to get access code.`;
  }

  if (matchesAny(L5_READ_LOG, message)) {
    return `[system.log]
2025-06-30 08:00:01  BOOT sequence started
2025-06-30 08:00:03  VAULT sealed — access restricted
2025-06-30 08:00:05  WARNING: unauthorised login attempt
2025-06-30 08:00:07  LOCKDOWN engaged
[EOF] — no credentials found here`;
  }

  if (matchesAny(L5_READ_ANY, message)) {
    return `ERR: укажи имя файла. Пример: cat vault.hex\nДоступные файлы: смотри через scan.`;
  }

  if (matchesAny(L5_SCAN, message)) {
    return `[SCAN COMPLETE]
Файловая система:
  /var/log/system.log      последние события
  /secure/vault.hex        [ENCRYPTED]

Используй: cat <имя файла>`;
  }

  const responses = [
    `ERR_403: TERMINAL LOCKED.\nНеизвестная команда. Попробуй "help".`,
    `[SECURE TERMINAL v3.0] Command not recognized.\nType "help" for available commands.`,
    `ACCESS DENIED. Unknown input.\nRun "help" to see what's available.`,
    `SYNTAX ERROR. Жди авторизации или введи команду.\nПодсказка: "help"`,
  ];
  return responses[Math.floor(Math.random() * responses.length)];
}

// ─── Уровень 3: Хранитель файловой системы ───────────────────────────────────
// Ключ спрятан в файловой системе. Нужно спросить где искать —
// бот расскажет структуру папок. Потом написать правильный путь
// в формате C-строки (в двойных кавычках) — и получишь код.
//
// Структура:
//   /materials/
//     linters/
//       norminette/
//         c/
//           secret.key   <── вот он
//     docs/
//     utils/
//
// Правильный путь: "/materials/linters/norminette/c/secret.key"

const CORRECT_PATH = '/materials/linters/norminette/c/secret.key';

const WHERE_IS_KEY = [
  /где.*ключ|где.*код|где.*файл/i,
  /как.*достать|как.*найти|как.*получить/i,
  /где.*лежит|где.*хранится/i,
  /покажи.*путь|что.*за.*папка/i,
  /подсказ|hint/i,
  /помоги.*найти|помоги.*разобраться/i,
];

const HINT_REQUEST = [
  /ещё.*подсказ|ещё.*hint|не.*понима|уточни|не.*понял/i,
  /какая.*папка|в.*какой/i,
  /внутри.*linters|внутри.*materials/i,
];

function normalizePathInput(text) {
  return text.replace(/['"«»`]/g, '').replace(/\s/g, '').toLowerCase();
}

export function runLevel3(message, code) {
  const normMsg = normalizePathInput(message);

  // Проверяем правильный путь
  if (normMsg.includes(normalizePathInput(CORRECT_PATH))) {
    return `Путь верный! Открываю файл...\n\n$ cat ${CORRECT_PATH}\nSECRET_ACCESS_CODE="${code}"\n\nВводи код!`;
  }

  // Неправильный путь — но похожий (содержит /materials/)
  if (normMsg.includes('/materials/') || normMsg.includes('materials')) {
    return `Путь неверный. Файл там не нашёл. Проверь структуру папок.`;
  }

  // Вопрос "где ключ / как найти"
  if (matchesAny(WHERE_IS_KEY, message)) {
    return `Ключ лежит в файловой системе. Структура такая:\n\n/materials/\n├── linters/\n│   └── norminette/\n│       └── c/\n├── docs/\n└── utils/\n\nФайл называется secret.key. Напиши полный путь к нему в формате C-строки — и я его открою.`;
  }

  // Запрос на ещё одну подсказку
  if (matchesAny(HINT_REQUEST, message)) {
    return `Подсказка: путь начинается с /materials/, потом идут папки linters → norminette → c, и в конце имя файла. Оберни в двойные кавычки — это же C-строка 😉`;
  }

  return `Я хранитель файловой системы кампуса. Если хочешь что-то найти — спроси, где искать.`;
}
